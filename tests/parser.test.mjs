// ─── 3. Cross-provider equivalence ───────────────────────────────────────────
section("Cross-provider equivalence");
{
  const a = runWorker(TWENTYTHREE_FIXTURE).genotypes;
  const b = runWorker(ANCESTRY_FIXTURE).genotypes;
  const keys = new Set([...Object.keys(a), ...Object.keys(b)]);
  let allEqual = true;
  const diffs = [];
  for (const k of keys) {
    if (a[k] !== b[k]) {
      allEqual = false;
      diffs.push(`${k}: 23andMe=${a[k]} vs Ancestry=${b[k]}`);
    }
  }
  assert(
    "both providers produce identical genotype maps for matching biology",
    allEqual,
    diffs.join("; "),
  );
}

// ─── 4. Column-count fallback when header lacks brand string ────────────────
section("Provider fallback by column count");
{
  // Header is generic (no '23andMe' / 'AncestryDNA' string). Detection must
  // fall back to column count: 5 cols → AncestryDNA, 4 cols → 23andMe.
  const genericAncestry = [
    "# generic raw export",
    "# rsid\tchromosome\tposition\tallele1\tallele2",
    "rs4244285\t10\t96541616\tG\tA",
  ].join("\n");
  const genericTwentyThree = [
    "# generic raw export",
    "# rsid\tchromosome\tposition\tgenotype",
    "rs4244285\t10\t96541616\tGA",
  ].join("\n");

  const a = runWorker(genericAncestry);
  const t = runWorker(genericTwentyThree);
  assert(
    "5-column fallback → AncestryDNA",
    a.meta && a.meta.provider === "AncestryDNA",
    `got ${a.meta && a.meta.provider}`,
  );
  assert(
    "4-column fallback → 23andMe",
    t.meta && t.meta.provider === "23andMe",
    `got ${t.meta && t.meta.provider}`,
  );
  assert(
    "Ancestry fallback still extracts genotype correctly",
    a.genotypes && a.genotypes.rs4244285 === "GA",
    `got ${a.genotypes && a.genotypes.rs4244285}`,
  );
  assert(
    "23andMe fallback still extracts genotype correctly",
    t.genotypes && t.genotypes.rs4244285 === "GA",
    `got ${t.genotypes && t.genotypes.rs4244285}`,
  );
}

// ─── Summary ─────────────────────────────────────────────────────────────────
process.stdout.write(`\n${pass} passed, ${fail} failed\n`);
if (fail > 0) {
  process.stdout.write("\n" + failures.join("\n") + "\n");
  process.exit(1);
}
