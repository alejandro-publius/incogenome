// Unit tests for src/parser.worker.js — provider detection + genotype
// extraction for both 23andMe (4-col) and AncestryDNA (5-col) layouts.
//
// Run with: node tests/parser.test.mjs   (or: make parser-test)
//
// The worker is designed to run in a browser Web Worker scope (it references
// `self.onmessage` / `self.postMessage` at module top level). To exercise it
// in Node without altering its contract, we load the source as text and run
// it inside a vm sandbox that exposes a fake `self` matching the Worker API.
// The driver then `postMessage`s a parse request and captures the reply,
// exactly as src/main.js does at runtime.

import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import vm from "node:vm";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const WORKER_SRC = readFileSync(join(ROOT, "src/parser.worker.js"), "utf8");

// ─── Worker harness ──────────────────────────────────────────────────────────
// Build a fresh sandbox per parse call so onmessage state can't leak between
// tests. Returns the single response posted back by the worker.
function runWorker(fileText) {
  let onmessage = null;
  const posted = [];
  const fakeSelf = {
    set onmessage(fn) { onmessage = fn; },
    get onmessage() { return onmessage; },
    postMessage: (msg) => posted.push(msg),
  };
  const context = { self: fakeSelf };
  vm.createContext(context);
  vm.runInContext(WORKER_SRC, context, { filename: "parser.worker.js" });
  if (typeof onmessage !== "function") {
    throw new Error("worker did not register self.onmessage");
  }
  onmessage({ data: { type: "parse", fileText } });
  if (posted.length !== 1) {
    throw new Error(`expected 1 postMessage, got ${posted.length}`);
  }
  return posted[0];
}

// ─── Test runner ─────────────────────────────────────────────────────────────
let pass = 0;
let fail = 0;
const failures = [];

function assert(name, condition, detail = "") {
  if (condition) {
    pass++;
    process.stdout.write(`  ✓ ${name}\n`);
  } else {
    fail++;
    failures.push(`✗ ${name}${detail ? `\n    ${detail}` : ""}`);
    process.stdout.write(`  ✗ ${name}\n`);
  }
}

function section(title) {
  process.stdout.write(`\n${title}\n`);
}

// ─── Synthetic fixtures ──────────────────────────────────────────────────────
// The same biological genotype calls expressed in each provider's layout.
// Includes: all 10 target rsIDs, an off-target row (to verify filtering),
// one explicit no-call per provider, one indel-style no-call (DD/II), and
// one missing target (rs1142345 omitted entirely → should be absent from
// the output map, contributing to "not determined" downstream).

const COMMON_EXPECTED = {
  rs4244285:  "GA",
  rs4986893:  "GG",
  rs12248560: "CT",
  rs1799853:  "CC",
  rs1057910:  "AA",
  rs9923231:  "CT",
  rs4149056:  "TC",
  rs1800462:  "CC",
  rs1800460:  "GG",
  // rs1142345 intentionally absent (no-call in fixtures).
};

const TWENTYTHREE_FIXTURE = [
  "# This data file generated by 23andMe at: Sat Jun 20 11:00:00 2026",
  "# https://you.23andme.com/p/yourname/tools/data/",
  "# rsid\tchromosome\tposition\tgenotype",
  "rs3094315\t1\t752566\tAG",            // off-target, must be ignored
  "rs4244285\t10\t96541616\tGA",
  "rs4986893\t10\t96540410\tGG",
  "rs12248560\t10\t96521657\tCT",
  "rs1799853\t10\t96702047\tCC",
  "rs1057910\t10\t96741053\tAA",
  "rs9923231\t16\t31107689\tCT",
  "rs4149056\t12\t21331549\tTC",
  "rs1800462\t6\t18143955\tCC",
  "rs1800460\t6\t18139228\tGG",
  "rs1142345\t6\t18130687\t--",          // explicit 23andMe no-call
  "rs9999999\t1\t1000000\tII",            // off-target indel, must be ignored
  "",
].join("\n");

const ANCESTRY_FIXTURE = [
  "#AncestryDNA raw data download",
  "#",
  "#This file was generated by AncestryDNA at: 06/20/2026 11:00:00",
  "#Genome build: GRCh37",
  "#rsid\tchromosome\tposition\tallele1\tallele2",
  "rs3094315\t1\t752566\tA\tG",          // off-target, must be ignored
  "rs4244285\t10\t96541616\tG\tA",
  "rs4986893\t10\t96540410\tG\tG",
  "rs12248560\t10\t96521657\tC\tT",
  "rs1799853\t10\t96702047\tC\tC",
  "rs1057910\t10\t96741053\tA\tA",
  "rs9923231\t16\t31107689\tC\tT",
  "rs4149056\t12\t21331549\tT\tC",
  "rs1800462\t6\t18143955\tC\tC",
  "rs1800460\t6\t18139228\tG\tG",
  "rs1142345\t6\t18130687\t0\t0",        // explicit Ancestry per-allele no-call
  "rs9999998\t1\t1000001\tD\tD",          // off-target indel, must be ignored
  "",
].join("\n");

// ─── 1. 23andMe parsing ──────────────────────────────────────────────────────
section("23andMe fixture");
{
  const reply = runWorker(TWENTYTHREE_FIXTURE);
  assert(
    "reply type is 'result'",
    reply.type === "result",
    `got type=${reply.type} msg=${reply.message || ""}`,
  );
  assert(
    "provider detected as 23andMe",
    reply.meta && reply.meta.provider === "23andMe",
    `got provider=${reply.meta && reply.meta.provider}`,
  );
  for (const [rsid, expected] of Object.entries(COMMON_EXPECTED)) {
    assert(
      `${rsid} = ${expected}`,
      reply.genotypes && reply.genotypes[rsid] === expected,
      `got ${reply.genotypes && reply.genotypes[rsid]}`,
    );
  }
  assert(
    "rs1142345 no-call ('--') is absent from output map",
    reply.genotypes && !("rs1142345" in reply.genotypes),
    `got ${reply.genotypes && reply.genotypes.rs1142345}`,
  );
  assert(
    "off-target rs3094315 is filtered out",
    reply.genotypes && !("rs3094315" in reply.genotypes),
  );
  assert(
    "no_call_count incremented for the '--' row",
    reply.meta && reply.meta.no_call_count >= 1,
    `got no_call_count=${reply.meta && reply.meta.no_call_count}`,
  );
}

// ─── 2. AncestryDNA parsing ──────────────────────────────────────────────────
section("AncestryDNA fixture");
